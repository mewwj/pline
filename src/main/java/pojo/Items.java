package pojo;

public class Items {

    private String testm;
    private int pnumber;
    private String time;
    private int good;
    private int ng;
    private Double goodrate;
    private Double ngrate;
    private int spc;
    private int thd;
    private int oth;
    public Items() {
    	
    }
    public String getTestm() {
		return testm;
	}
	public void setTestm(String testm) {
		this.testm = testm;
	}
	public int getPnumber() {
		return pnumber;
	}
	public void setPnumber(int pnumber) {
		this.pnumber = pnumber;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public int getGood() {
		return good;
	}
	public void setGood(int good) {
		this.good = good;
	}
	public int getNg() {
		return ng;
	}
	public void setNg(int ng) {
		this.ng = ng;
	}
	public Double getGoodrate() {
		return goodrate;
	}
	public void setGoodrate(Double goodrate) {
		this.goodrate = goodrate;
	}
	public Double getNgrate() {
		return ngrate;
	}
	public void setNgrate(Double ngrate) {
		this.ngrate = ngrate;
	}
	public int getSpc() {
		return spc;
	}
	public void setSpc(int spc) {
		this.spc = spc;
	}
	public int getThd() {
		return thd;
	}
	public void setThd(int thd) {
		this.thd = thd;
	}
	public int getOth() {
		return oth;
	}
	public void setOth(int oth) {
		this.oth = oth;
	}
}
